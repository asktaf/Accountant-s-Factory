<br>
<img src="images/icon/img3.png" class="img-fluid" width="64">

<h4><b>At Least Two Designated Partners Are Mandatory

</b></h4>
<p>Every LLP must have at least two designated partners who are responsible for managing the business and ensuring legal compliance.

</p>
<br>
<img src="images/icon/img5.png" class="img-fluid" width="64">

<h4><b>One Designated Partner Must Be a Resident of India

</b></h4>
<p>At least one of the designated partners in the LLP must be a resident of India, ensuring local oversight and compliance with Indian regulations.




</p>
<br>

<img src="images/icon/img7.png" class="img-fluid" width="64">

<h4><b>Limited Liability for Each Partner


</b></h4>
<p>The liability of each partner is limited to their agreed contribution to the LLP, protecting their personal assets from business debts.

</p>
<br>

<br>

<img src="images/icon/compliance.png" class="img-fluid" width="64">

<h4><b>Less Compliance and Regulatory Burden



</b></h4>
<p>LLPs are subject to fewer compliance requirements and regulations, making them easier to manage and operate on a day-to-day basis.



</p>




